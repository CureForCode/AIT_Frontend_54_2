//Функция Number преобразует строку в число, чтобы была возможность проверки по условиям if else.
// Если использовать только prompt, то введённое пользователем значение вернется в формате String и дальнейший if else код не получится выполнить.
// Пример через prompt: пользователь вводит число 15, возвращается строка "15", которую невозможно прогнать через if else.
//Если использовать Number, то строка "15" будет преобразована в число 15 и следовательно далее будет выполняться блок if else кода.

let temperatureInput = prompt(
  "Введите температуру для проверки (пример: -5, 0, 24)"
);

// Проверяем, отменил ли пользователь ввод
if (temperatureInput === null) {
  console.log(
    "Вы отказались вводить данные. Пожалуйста, перезагрузите страницу и попробуйте еще раз!"
  );
  return;
}

// Преобразуем ввод в число и проверяем валидность
let temperature = Number(temperatureInput);

// Проверяем, является ли введенное значение числом
if (isNaN(temperature)) {
  console.log(
    "Ошибка: Введено некорректное значение. Пожалуйста, введите число (пример: -5, 0, 24)."
  );
  return;
}

// Проверяем диапазоны температур
if (temperature < 0) {
  console.log("Холодно!");
} else if (temperature >= 0 && temperature <= 20) {
  console.log("Прохладно");
} else if (temperature >= 21 && temperature <= 30) {
  console.log("Тепло");
} else {
  console.log("Жарко!");
}
